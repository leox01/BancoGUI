    private void registrarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registrarButtonActionPerformed
        
        String id = idTextField.getText();
        String nombre = nombreTextField.getText();
        String direccion = direccionTextField.getText();
        String email = emailTextField.getText();
        String telefono = telefonoTextField.getText();
        String numeroCuenta = numeroCuentaTextField.getText();
        String saldo = saldoTextField.getText();
        String datosClienteArray[] = {id,nombre,direccion,email,numeroCuenta,saldo,telefono};
        
  
        if(hayDatosSinLlenar(datosClienteArray)){
            mostrarMensaje("Error","Por favor llena todos los datos");
        }else{
            //Voy a revisar si los datos cumplen con los requerimientos 
              if(id.length()>=4){ //1.- Tamaño ID
                   try{
                        int numeroCuentaInt = Integer.parseInt(numeroCuenta);
                        double saldoDouble = Double.parseDouble(saldo);
                       
                        if(Controlador.datosRepetidos(id, email, numeroCuentaInt)){  //2.- No estén repetidos
                            System.out.println("Datos repetidos, intenta con otros");
                            String titulo = "Error";
                            String descripcion = "id, emial o número de cuenta repetidos";
                            mostrarMensaje(titulo, descripcion);
                            
                        }else{
                            Controlador.registrarCliente(id,nombre,direccion,telefono,numeroCuentaInt, saldoDouble, email);
                            mensajeLabel.setText("Usuario registrado exitosamente!!!");
                            limpiarTextFields();
                            dibujarUsuariosEnLaTabla();
                        }

                        
                    }catch(Exception e){
                        System.out.println("Error al intentar registrar el cliente por el saldo o el numero de cuenta");
                    }
                  
              }else
                  mostrarMensaje("Error", "ID incorrecto, debe de contener 4 caractéres");   
        }
    }//GEN-LAST:event_registrarButtonActionPerformed

    private void buscarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buscarButtonActionPerformed
        String numeroDeCuentaBuscada = buscarNumeroCuentaTextField.getText();
        String numeroCuentaBuscadaArray[] = {numeroDeCuentaBuscada};
        
        if(hayDatosSinLlenar(numeroCuentaBuscadaArray)){
            mostrarMensaje("Error", "Ingresa una cuenta a buscar");
        }else{
        
            try {
                int numeroDeCuentaInt = Integer.parseInt(numeroDeCuentaBuscada);
                dibujarUsuarioBuscadoEnLaTabla(Controlador.buscarUsuario(numeroDeCuentaInt,"poblar"));
                
            } catch (Exception e) {
                mostrarMensaje("Error", "No es un número entero");
                
            }
        }
    }//GEN-LAST:event_buscarButtonActionPerformed

