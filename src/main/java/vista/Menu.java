    private boolean hayDatosSinLlenar(String datosClienteArray[]){
        
        for (String datoClienteArray : datosClienteArray) {
            if (datoClienteArray.equals("")) {
                return true;
            }
        }
        return false;
    }
    
    
    
    
    
